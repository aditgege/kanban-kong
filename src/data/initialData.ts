export function buildInitialColumns(): Record<string, Column> {
  const columns: Record<string, Column> = {};
  const columnIds: ColumnId[] = ['todo', 'doing', 'review', 'done', 'rework'];

  for (const colId of columnIds) {
    const config = COLUMN_CONFIG[colId];
    columns[colId] = {
      id: colId,
      title: config.title,
      color: config.color,
      taskIds: INITIAL_TASKS
        .filter((t) => t.columnId === colId)
        .sort((a, b) => a.order - b.order)
        .map((t) => t.id),
    };
  }

  return columns;
}

export function buildInitialTasks(): Record<string, Task> {
  const tasks: Record<string, Task> = {};
  for (const task of INITIAL_TASKS) {
    tasks[task.id] = task;
  }
  return tasks;
}
